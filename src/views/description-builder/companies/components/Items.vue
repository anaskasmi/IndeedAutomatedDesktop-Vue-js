<template>
  <v-card elevation="0"  rounded="lg" class="my-4"  :loading="isLoading" :disabled="isLoading">
    <CreateItemDialog />
    <v-card-title style="color: #30475e">Companies</v-card-title>
    <v-card-text class="pa-12">
      <v-data-table
        :headers="headers"
        :items="items"
        item-key="id"
        class="elevation-0"
        :search="search"
      >
        <template v-slot:top>
          <v-row>
            <v-text-field
              v-model="search"
              label="Search"
              class="mx-4"
              color="#30475e"
            ></v-text-field>
            <v-btn
              dark
              color="#30475e"
              @click="createItemDialogVisible = true"
              class="my-6"
              small
              fab
              ><v-icon medium>mdi-plus</v-icon></v-btn
            >
          </v-row>
        </template>
        <template class="align-center" v-slot:[`item.name`]="{ item }">
          <EditableText :item="item" />
        </template>
        <template v-slot:[`item.actions`]="{ item }">
          <v-icon
            class="mr-2"
            medium
            color="green darken-2"
            @click="editItem(item)"
          >
            mdi-pencil
          </v-icon>
          <v-icon medium color="red darken-2" @click="deleteItem(item)">
            mdi-delete
          </v-icon>
        </template>
      </v-data-table>
    </v-card-text>
  </v-card>
</template>

<script>
import EditableText from "@/views/description-builder/positions/components/items/Editable.vue";
import CreateItemDialog from "@/views/description-builder/positions/components/items/CreateItem.vue";
export default {
  created() {
    this.fetchItems();
  },
  components: {
    EditableText,
    CreateItemDialog,
  },
  data() {
    return {
      isLoading: false,
      search: null,
      headers: [
        {
          text: "name",
          align: "start",
          sortable: true,
          value: "name",
        },
        {
          text: "actions",
          align: "end",
          sortable: true,
          value: "actions",
          width: "10%",
        },
      ],
    };
  },
  computed: {
    items: {
      get: function () {
        return this.$store.getters["DescriptionBuilderPositionsModule/items"];
      },
      set: function (newVal) {
        this.$store.commit("DescriptionBuilderPositionsModule/items", newVal);
      },
    },
    createItemDialogVisible: {
      get: function () {
        return this.$store.getters[
          "DescriptionBuilderPositionsModule/createItemDialogVisible"
        ];
      },
      set: function (newVal) {
        this.$store.commit(
          "DescriptionBuilderPositionsModule/createItemDialogVisible",
          newVal
        );
      },
    },
  },
  methods: {
    async fetchItems() {
      this.isLoading = true;
      try {
        await this.$store.dispatch(
          "DescriptionBuilderPositionsModule/fetchPositionsItems"
        );
      } catch (error) {
        console.log(error);
      } finally {
        this.isLoading = false;
      }
    },
    editItem(company) {
      this.editDialogOpen = true;
      this.companyUnderEdit = company;
    },
    async deleteItem(item) {
      this.$swal
        .fire({
          title: "This Item will be deleted, continue ?",
          showCancelButton: true,
          confirmButtonText: "Delete",
          cancelButtonText: `Cancel`,
          confirmButtonColor: "#E05D5D",
          cancelButtonColor: "#ddd",
          icon: "error",
        })
        .then(async (result) => {
          if (result.isConfirmed) {
            this.isLoading = true;
            try {
              await this.$store.dispatch(
                "DescriptionBuilderPositionsModule/deletePositionItem",
                item._id
              );
              this.$swal.fire({
                title: "Deleted successfully",
                icon: "success",
                confirmButtonColor: "#219F94",
              });
            } catch (error) {
              this.$store.dispatch("alertsModule/fireAxiosError", error);
            } finally {
              this.isLoading = false;
            }
          }
        });
    },
  },
};
</script>